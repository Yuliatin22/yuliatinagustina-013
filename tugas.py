# mengonversi suhu celcius ke suhu fahrenheit

print("mengonversi suhu celcius ke suhu fahrenheit")
c = int(input("masukkan nilai celcius"))
konversi = (c*9/5)+32
print("hasil konversi suhu celcius ke suhu fahrenheit",konversi )

